package _5_Access_modifier_in_java.exercise.bt;

public class Circle {
    private double radius = 1.0;
    private String color = "red";
    public Circle(){

    }
    public Circle(double radius){
        this.radius = radius;
    }
    public double getRadius(){
        return radius;
    }
    public String getArea(){
        return color;
    }

}
